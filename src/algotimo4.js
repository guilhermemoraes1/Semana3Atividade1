// cria um arraylist chamado arr
const arr = [1,2,3,4,5]

// função filter itera os elementos de arr e retorna apenas aqueles que forem maiores que 4
// atribuindo esse array com apenas um valor na variável output
const output = arr.filter((x)=>  x > 4);

// mostra no console o valor de output
console.log(output);

